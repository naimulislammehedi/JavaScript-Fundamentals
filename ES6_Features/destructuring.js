// array 
const color = ["Red", "Green", "Blue"]; 

const [first, second] = color; 

console.log(first); // red 
console.log(second); // green

// extract values easily from arrays or objects

// object 
const user = {
    name: "Mehedi", 
    age: 25
}; 

const {name, age} = user; 

console.log(name); // Mehedi
console.log(age); // 25